% Module name.
-module(jdlib).

% Export.
-export([fst/1, snd/1]).

%---------------------------------------------------------------------------------------------------
% Types.
%---------------------------------------------------------------------------------------------------

% Types export.
-export_type([pair/0]).

% Pair.
-type pair() :: {term(), term()}.

%---------------------------------------------------------------------------------------------------
% Functions.
%---------------------------------------------------------------------------------------------------

-spec fst(P :: pair()) -> term().
%% @doc
%% Get first element of tuple.
fst({T, _}) ->
    T;
fst(P) ->
    throw({badarg, P}).

%---------------------------------------------------------------------------------------------------

-spec snd(P :: pair()) -> term().
%% @doc
%% Get second element of tuple.
snd({_, T}) ->
    T;
snd(P) ->
    throw({badarg, P}).

%---------------------------------------------------------------------------------------------------

