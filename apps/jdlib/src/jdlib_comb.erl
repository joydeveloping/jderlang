-spec c(K :: integer(), N :: integer()) -> integer().
%% @doc
%% C(k, n).
c(K, N) ->
    Num = jdlib_lists:product(lists:seq(N, N - K + 1, -1)),
    Den = jdlib_lists:product(lists:seq(1, K)),
    Num div Den.

